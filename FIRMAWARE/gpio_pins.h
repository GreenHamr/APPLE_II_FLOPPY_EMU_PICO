/*
 * GPIO Pin Definitions and Descriptions
 * 
 * Този файл описва всички GPIO пинове, използвани в Apple II Floppy Disk Emulator
 * за Raspberry Pi Pico.
 * 
 * Всички пинове са конфигурируеми чрез структурата gpio_config_t в config.h
 */

#ifndef GPIO_PINS_H
#define GPIO_PINS_H

// ============================================================================
// Фазови сигнали (Stepper Motor Control)
// ============================================================================
// Тези пинове се използват за управление на степер мотора на флопи диска
// Apple II контролерът управлява фазите директно чрез 4-фазен степер мотор
//
// PH0 (Phase 0) - GPIO 0 (по подразбиране)
//   - Фаза 0 на степер мотора
//   - Насочване: INPUT (чете се от Apple II контролер)
//
// PH1 (Phase 1) - GPIO 1 (по подразбиране)
//   - Фаза 1 на степер мотора
//   - Насочване: INPUT (чете се от Apple II контролер)
//
// PH2 (Phase 2) - GPIO 2 (по подразбиране)
//   - Фаза 2 на степер мотора
//   - Насочване: INPUT (чете се от Apple II контролер)
//
// PH3 (Phase 3) - GPIO 3 (по подразбиране)
//   - Фаза 3 на степер мотора
//   - Насочване: INPUT (чете се от Apple II контролер)

// ============================================================================
// Сигнали от Apple II контролер (INPUT)
// ============================================================================
// Тези пинове се използват за получаване на команди от Apple II компютъра
//
// MOTOR_ON - GPIO 5 (по подразбиране)
//   - Сигнал за включване/изключване на мотора
//   - Насочване: INPUT
//   - Логика: HIGH = мотор включен, LOW = мотор изключен
//
// WRITE_ENABLE - GPIO 6 (по подразбиране)
//   - Сигнал за разрешаване на запис
//   - Насочване: INPUT
//   - Логика: HIGH = запис разрешен, LOW = запис забранен
//
// WRITE_DATA - GPIO 7 (по подразбиране)
//   - Данни за запис от Apple II контролер
//   - Насочване: INPUT
//   - Формат: GCR (Group Code Recording) данни при 125 kHz
//   - Обработка: PIO state machine за прецизно времево управление

// ============================================================================
// Сигнали към Apple II контролер (OUTPUT)
// ============================================================================
// Тези пинове се използват за изпращане на данни и статус към Apple II компютъра
//
// READ_DATA - GPIO 8 (по подразбиране)
//   - Данни за четене към Apple II контролер
//   - Насочване: OUTPUT
//   - Формат: GCR (Group Code Recording) данни при 125 kHz
//   - Обработка: PIO state machine за прецизно времево управление
//
// TRACK0 - GPIO 9 (по подразбиране)
//   - Индикация за позиция на пътека 0
//   - Насочване: OUTPUT
//   - Логика: LOW = на пътека 0, HIGH = не на пътека 0
//   - Active LOW сигнал
//
// WRITE_PROTECT - GPIO 10 (по подразбиране)
//   - Индикация за защита от запис
//   - Насочване: OUTPUT
//   - Логика: LOW = защитено (write protected), HIGH = разрешено (write enabled)
//   - Може да се конфигурира софтуерно

// ============================================================================
// SD Card SPI интерфейс
// ============================================================================
// Тези пинове се използват за комуникация със SD картата чрез SPI протокол
//
// SD_MISO (Master In Slave Out) - GPIO 16 (по подразбиране)
//   - Данни от SD картата към Pico
//   - Насочване: INPUT
//   - SPI функция: MISO
//
// SD_CS (Chip Select) - GPIO 17 (по подразбиране)
//   - Избор на SD картата
//   - Насочване: OUTPUT
//   - Логика: LOW = избрана, HIGH = не избрана
//   - SPI функция: CS
//
// SD_SCK (Serial Clock) - GPIO 18 (по подразбиране)
//   - Тактов сигнал за SPI комуникация
//   - Насочване: OUTPUT
//   - SPI функция: SCK
//
// SD_MOSI (Master Out Slave In) - GPIO 19 (по подразбиране)
//   - Данни от Pico към SD картата
//   - Насочване: OUTPUT
//   - SPI функция: MOSI

// ============================================================================
// I2C интерфейс за OLED дисплей
// ============================================================================
// Тези пинове се използват за комуникация с SSD1306 OLED дисплей чрез I2C
//
// I2C_SDA (Serial Data) - GPIO 20 (по подразбиране)
//   - Данни за I2C комуникация
//   - Насочване: Bidirectional (open-drain)
//   - I2C функция: SDA
//
// I2C_SCL (Serial Clock) - GPIO 21 (по подразбиране)
//   - Тактов сигнал за I2C комуникация
//   - Насочване: OUTPUT (open-drain)
//   - I2C функция: SCL

// ============================================================================
// Rotary Encoder (Енкодер)
// ============================================================================
// Тези пинове се използват за управление на потребителския интерфейс
//
// ENCODER_A - GPIO 11 (по подразбиране)
//   - Канал A на ротационния енкодер
//   - Насочване: INPUT
//   - Използва се за определяне на посоката на въртене
//
// ENCODER_B - GPIO 12 (по подразбиране)
//   - Канал B на ротационния енкодер
//   - Насочване: INPUT
//   - Използва се за определяне на посоката на въртене
//
// ENCODER_BUTTON - GPIO 13 (по подразбиране)
//   - Бутон на ротационния енкодер
//   - Насочване: INPUT
//   - Логика: LOW = натиснат, HIGH = не натиснат
//   - Използва се за избор на меню опции

// ============================================================================
// LED индикатор
// ============================================================================
// Този пин се използва за визуална индикация на състоянието на системата
//
// LED - GPIO 25 (по подразбиране)
//   - LED индикатор (вградена LED на Pico)
//   - Насочване: OUTPUT
//   - Логика: HIGH = свети, LOW = изключен
//   - Използва се за индикация на работа и грешки

// ============================================================================
// Резюме на пиновете по подразбиране
// ============================================================================
//
// Фазови сигнали:        GPIO 0-3
// Apple II входни:       GPIO 5-7
// Apple II изходни:      GPIO 8-10
// Енкодер:               GPIO 11-13
// SD Card SPI:           GPIO 16-19
// I2C OLED:              GPIO 20-21
// LED:                   GPIO 25
//
// Общо използвани пинове: 19
// Свободни пинове:         GPIO 4, 14, 15, 22, 23, 24, 26, 27, 28

#endif // GPIO_PINS_H

